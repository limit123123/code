public class Graduation {
	private int regno;
	private String leveltaken;
	private String degreeclass;
	public int getRegno() {
		return regno;
	}
	public void setRegno(int regno) {
		this.regno = regno;
	}
	public String getLeveltaken() {
		return leveltaken;
	}
	public void setLeveltaken(String leveltaken) {
		this.leveltaken = leveltaken;
	}
	public String getDegreeclass() {
		return degreeclass;
	}
	public void setDegreeclass(String degreeclass) {
		this.degreeclass = degreeclass;
	}
	
}
